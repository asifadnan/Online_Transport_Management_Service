<p >Formar Bangladesh Institute of Technology (BIT) Chittagong is presently Chittagong University of Engineering & Technology (CUET). It is one of the prominent and prestigious autonomous self-degree-awarding University of Bangladesh in the field of Engineering Education. University was started as the Engineering College, Chittagong that was established in 1968. Engineering College, Chittagong was under the Faculty of Engineering, University of Chittagong. Through a Government Ordinance in 1986, Engineering College, Chittagong was converted into BIT, Chittagong. In September-2003 BIT, Chittagong was converted into a Public University. The Honorable President of the Peoples Republic of Bangladesh is the Chancellor of the University.CUET provides well mannered transportation service to students,teachers,officie workers.
